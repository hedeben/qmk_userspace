// Animation parameters
#define FRAME_DURATION 150 // How long each frame lasts in milliseconds

// Animation variables
uint32_t timer = 0;
uint8_t current_frame = 0;

static void render_animation(void) {

    // oled_render_anim_frame(FAST_TYPE_FRAMES, SLOW_TYPE_FRAMES, 4);

    // Run animation
    if (timer_elapsed(timer) > FRAME_DURATION) {
        // Set timer to updated time
        timer = timer_read();
        
        // Increment frame
        current_frame = (current_frame + 1) % (sizeof(frames) / sizeof(frames[0]));

        // Draw frame to OLED
        oled_write_raw_P(frames[current_frame], frame_sizes[current_frame]); 
    }
}